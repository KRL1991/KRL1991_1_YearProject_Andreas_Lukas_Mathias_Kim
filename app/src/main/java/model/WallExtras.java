package model;

public class WallExtras {

    String acousticpanel;
    String satinGlass;
    String wetRoom;
    String soundGlass;
    String panelColour;

    public String getAcousticpanel() {
        return acousticpanel;
    }

    public void setAcousticpanel(String acousticpanel) {
        this.acousticpanel = acousticpanel;
    }

    public String getSatinGlass() {
        return satinGlass;
    }

    public void setSatinGlass(String satinGlass) {
        this.satinGlass = satinGlass;
    }

    public String getWetRoom() {
        return wetRoom;
    }

    public void setWetRoom(String wetRoom) {
        this.wetRoom = wetRoom;
    }

    public String getSoundGlass() {
        return soundGlass;
    }

    public void setSoundGlass(String soundGlass) {
        this.soundGlass = soundGlass;
    }

    public String getPanelColour() {
        return panelColour;
    }

    public void setPanelColour(String panelColour) {
        this.panelColour = panelColour;
    }
}
